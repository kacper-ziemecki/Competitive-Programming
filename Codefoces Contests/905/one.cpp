void solve()
{
  string text;
  cin >> text;
  int cur = 1, res = 0;
  for(auto el : text){
    int n = el-'0';
    if(n == 0) n = 10;
    res += abs(cur - n) + 1;
    cur = n;
  }
  cout << res << endl;
}
